//FOR LOOP
//for(initialize iterator; condition; increment)
for (let x = 1; x <= 6; x++) {
  console.log("x - ", x);
}

for (let x = 1; x <= 6; x++) {
  if (x % 2 == 0) {
    console.log("x - ", x);
  }
}

//WHILELOOP
//while(condition){execution}

let j = 0;
while (j < 6) {
  console.log("x -", j);
  j++;
}
